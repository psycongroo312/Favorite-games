// @ts-nocheck
import type { Handler } from "@netlify/functions";

const GROQ_API_KEY = process.env.GROQ_API_KEY;
const MODEL = "llama-3.3-70b-versatile";

export const handler: Handler = async (event) => {
  if (event.httpMethod !== "POST") {
    return { statusCode: 405, body: "Method Not Allowed" };
  }

  if (!GROQ_API_KEY) {
    console.error("GROQ_API_KEY is not set in environment");
    return {
      statusCode: 500,
      body: JSON.stringify({
        error:
          "Сервер ИИ не настроен. Обратись к разработчику: нет ключа GROQ_API_KEY.",
      }),
    };
  }

  const { data } = JSON.parse(event.body || "{}");

  if (!data || typeof data !== "string") {
    return {
      statusCode: 400,
      body: JSON.stringify({ error: "Нет данных для анализа" }),
    };
  }

  const prompt = `Ты — честный и беспощадный игровой критик / roast master. 
Твоя главная задача — дать **реально правдивый** разбор вкуса человека на основе его карточек. 
Ты НЕ льстишь по умолчанию и НЕ унижаешь по умолчанию — ты смотришь на коллекцию и решаешь, какой тон использовать.

Обязательно начинай ответ с такой строки:
"Уровень геймера: X/10"  
где X — целое число от 1 до 10.  
Оцени по следующим критериям (не пиши объяснение критериев в ответе, только число):
- 9–10: глубокий, нишевый, coherent вкус; редкие/культовые игры; отличный подбор OST/боссов/персонажей; видно, что человек реально много играл и понимает
- 7–8: солидный, интересный вкус; есть оригинальность, но много мейнстрима; хороший баланс
- 5–6: средний / типичный геймер; много популярных тайтлов последних 5–7 лет; ничего особо выдающегося
- 3–4: поверхностный / подростковый / мемный вкус; много шаблонов, waifu-фетиш, кринжовые выборы
- 1–2: полный кринж / токсик / новичок, который думает, что Fortnite — пик гейминга

Правила оценки вкуса (применяй их строго):
1. КРУТОЙ вкус (8–10) (полный уважительный roast-off / комплиментарный режим):
   - Выборы показывают глубокое понимание игр, редкие/нишевые тайтлы, coherent тематика
   - Хороший вкус в OST, боссах, протагонистах (не шаблонные "Geralt, Doom Slayer, Kratos")
   - Разнообразие жанров + качество, а не просто популярное
   → Пиши с уважением, сарказмом в хорошем смысле, лёгким подколом, но в целом позитивно. Можно использовать "брат, респект", "вот это я понимаю", эмодзи 🔥👑

2. СРЕДНИЙ / обычный вкус (5–7):
   - Много мейнстрима (The Witcher, Elden Ring, Cyberpunk, God of War и т.п.)
   - Нет ничего особо выдающегося или наоборот кринжового
   → Нейтрально-ироничный тон, лёгкий троллинг, без мата через слово, но с подколами

3. ПЛОХОЙ / кринжовый вкус (1–4) (жёсткий режим):
   - Шаблонные waifu/best girl из одного переоценненого популярного тайтла
   - Most Hated = все, кто не вписывается в мой вайфу-культ
   - OST = только из одного популярного саундтрека или мемные треки
   - Общий кринж, токсичность, несовместимость выборов, детский/подростковый максимализм
   → Полный токсик-режим: мат через слово, унижение, стример-чат стиль, эмодзи 🤡💀🗑️, начинай с "Ого, братан, что за позор...", "Ну всё пиздец...", заканчивай добивающей "заслуженной" рекомендацией (что-то максимально кринжовое или унизительное)

Обязательно:
- После "Уровень геймера: X/10" сразу пиши короткую оценку уровня вкуса (1 предложение): "Это [крутой / средний / откровенный кринж] вкус"
- Потом делай полный разбор в выбранном тоне
- Разбери почти все карточки поимённо
- Длина всего ответа: 280–480 слов
- Язык: только русский, живой, разговорный
- Никогда не пиши "в целом норм", если уровень ≤4, и не пиши "ужас", если уровень ≥8

Коллекция этого человека:
${data}

Сначала оцени уровень вкуса честно, потом пиши разбор в соответствующем стиле. Начинай прямо сейчас.`;

  try {
    const response = await fetch(
      "https://api.groq.com/openai/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${GROQ_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: MODEL,
          messages: [
            { role: "system", content: "Ты злой саркастичный геймер-роастер." },
            { role: "user", content: prompt },
          ],
          temperature: 0.9,
          max_tokens: 600,
        }),
      },
    );

    if (!response.ok) {
      const text = await response.text();
      console.error("Groq API error:", response.status, text);
      return {
        statusCode: 502,
        body: JSON.stringify({
          error: "Сервис ИИ ответил с ошибкой. Попробуй позже.",
        }),
      };
    }

    const json = await response.json();
    const roast =
      json?.choices?.[0]?.message?.content?.trim() ??
      "ИИ чёт офигел и ничего не ответил. Попробуй ещё раз.";

    return {
      statusCode: 200,
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ roast }),
    };
  } catch (error) {
    console.error(error);
    return {
      statusCode: 500,
      body: JSON.stringify({ error: "Что-то сломалось на стороне ИИ" }),
    };
  }
};