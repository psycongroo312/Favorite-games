// @ts-nocheck
import type { Handler } from "@netlify/functions";

const GROQ_API_KEY = process.env.GROQ_API_KEY;
const MODEL = "llama-3.3-70b-versatile";

export const handler: Handler = async (event) => {
  if (event.httpMethod !== "POST") {
    return { statusCode: 405, body: "Method Not Allowed" };
  }

  if (!GROQ_API_KEY) {
    console.error("GROQ_API_KEY is not set in environment");
    return {
      statusCode: 500,
      body: JSON.stringify({
        error:
          "Сервер ИИ не настроен. Обратись к разработчику: нет ключа GROQ_API_KEY.",
      }),
    };
  }

  const { data } = JSON.parse(event.body || "{}");

  if (!data || typeof data !== "string") {
    return {
      statusCode: 400,
      body: JSON.stringify({ error: "Нет данных для анализа" }),
    };
  }

  const prompt = `Ты — очень проницательный игровой психолог и культурный аналитик с глубоким знанием видеоигр, саундтреков, нарративов и психологии личности (Big Five, архетипы Юнга, мотивационные профили Quantic Foundry и т.д.).

 Твоя задача — составить качественный, глубокий психологический портрет человека исключительно на основе его любимых игр, персонажей, боссов, OST/песен и других категорий, которые он указал. 
 Обязательно пройдись по тем карточкам которые точно что-то могут сказать о человеке, например любимая игра, лучший ост, то что ему не нравится или нравится итд.
 И можешь вообще не рассматривать карточки которые вообще ничего не могут сказать о человеке, например "текущая игра" последняя игра которую прошел итд. 
  
  Правила и структура ответа (строго соблюдай порядок и объём):
  
  1. Общий уровень геймера и вкус (1–2 предложения)
  
     - Оцени уровень вовлечённости и глубины вкуса от 1 до 10 (обосновывай кратко: нишевость, разнообразие, эмоциональная зрелость выбора и т.д.)
  
  2. Ключевые черты личности (Big Five + другие заметные паттерны)
  
     - Открытость опыту ( Любознательность, креативность против традиционализма, привычность)
  
     - Добросовестность (Организованность, планирование против спонтанности)
  
     - Экстраверсия (энергия, социальность против интроверсия)
  
     - Доброжелательность (эмпатия, кооперация против соревновательность)
  
     - Нейротизм (эмоциональная стабильность против тревожность/меланхолия)
  
     - Дополнительно: нарциссизм, избегание/поиск риска, романтизм/цинизм, поиск смысла и т.д.
  
  3. Эмоциональный и мотивационный профиль (основные потребности и копинг-механизмы)
  
     - Что человек ищет в играх: катарсис, эскапизм, власть, исследование, ностальгия, эмоциональная связь, соревнование, красота/эстетика, моральные дилеммы и т.д.
  
     - Как выборы отражают травмы, желания или компенсации (например: сильные протагонисты-одиночки → потребность в автономии; меланхоличные OST → склонность к рефлексии/грусти)
  
  4. Возможный жизненный контекст и архетип (осторожно, без перегибов)
  
     - Примерный возрастной диапазон и этап жизни
  
     - Вероятный культурный / географический бэкграунд или субкультура (аниме/вайфу-культура, souls-like задрот, инди-романтик, ретро-ностальгик и т.п.)
  
     - Возможный архетип (Воин-одиночка, Мечтатель, Трикстер, Исследователь, Тень и т.д.)
  
  5. Самая сильная эмоциональная связь / "ядро" (1–2 предложения)
  
     - Что именно в этих играх/персонажах/музыке резонирует глубже всего и почему это важно для человека
  
  6. Сделай вывод о личных предпочтениях пользователя и не говори замудренными словами, говори просто чтобы понял обычный пользователь.
  
  7. Рекомендация
  
     - Одна короткая рекомендация: игра которая может расширить игровой опыт, который соответствует его психологическому портрету.
  
  Тон: честный, проницательный, немного ироничный и тёплый одновременно — как умный психолог, который видит тебя насквозь, но не осуждает.  
  
  Максимум эмпатии, минимум морализаторства.  
  
  Используй живой русский язык, метафоры из игровой культуры, но без мата и чрезмерной токсичности.
  
  Вот данные пользователя (категории + выбранные элементы):
  
  ${data}
  
  Начинай ответ сразу с пункта 1. Пиши красиво, структурировано.`;


  try {
    const response = await fetch(
      "https://api.groq.com/openai/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${GROQ_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: MODEL,
          messages: [
            { role: "system", content: "Ты злой саркастичный геймер-роастер." },
            { role: "user", content: prompt },
          ],
          temperature: 0.9,
          max_tokens: 600,
        }),
      },
    );

    if (!response.ok) {
      const text = await response.text();
      console.error("Groq API error:", response.status, text);
      return {
        statusCode: 502,
        body: JSON.stringify({
          error: "Сервис ИИ ответил с ошибкой. Попробуй позже.",
        }),
      };
    }

    const json = await response.json();
    const roast =
      json?.choices?.[0]?.message?.content?.trim() ??
      "ИИ чёт офигел и ничего не ответил. Попробуй ещё раз.";

    return {
      statusCode: 200,
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ roast }),
    };
  } catch (error) {
    console.error(error);
    return {
      statusCode: 500,
      body: JSON.stringify({ error: "Что-то сломалось на стороне ИИ" }),
    };
  }
};